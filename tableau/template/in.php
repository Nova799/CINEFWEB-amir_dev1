  <script>
  document.addEventListener("DOMContentLoaded", function() {
    // Sélectionnez l'élément de saisie de recherche
    const searchInput = document.getElementById("searchInput");

    // Sélectionnez la liste des résultats
    const resultsList = document.getElementById("resultsList");

    // Créez une copie de la liste des résultats d'origine
    const originalList = resultsList.cloneNode(true);

    // Écoutez l'événement d'entrée de saisie de recherche
    searchInput.addEventListener("input", function(event) {
      const searchTerm = event.target.value.toLowerCase(); // Convertir en minuscules pour la correspondance insensible à la casse

      // Réinitialiser la liste des résultats
      resultsList.innerHTML = "";

      // Parcourir chaque élément de la liste à filtrer
      const items = originalList.querySelectorAll(".select-table tbody tr");
      items.forEach(function(item) {
        const textContent = item.textContent.toLowerCase();
        if (textContent.includes(searchTerm)) {
          // Ajouter l'élément à la liste des résultats
          resultsList.appendChild(item.cloneNode(true));
        }
      });
    });
  });
</script>


